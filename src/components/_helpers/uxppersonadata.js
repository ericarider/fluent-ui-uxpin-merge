    /**
     * Creates the params required for a basic Microsoft Fluent Persona. 
     * Useful for the Facepile component, for example. 
     * @param {string} key A value to use as the key. Can be a number or string.
     * @param {string} imgURL A full URL, including http://, or an empty string.
     * @param {string} initials Initials to display in the Persona Coin if there is no image.  
     * @param {string} fullName Full display name: first and last name
     * @param {string} presence A value from the Persona Presence enumeration, such as 'away' or 'online'. 
     * @param {string} initialsColor The color for the Persona Coin if there is no image. Must be one of the supported Persona Coin color enumerations. 
     * @param {string} email An email address 
     * @returns {Object} Returns a Microsoft Persona-compatible params object. 
     */
    configureBasePersona: function (key, imgURL, initials, fullName, presence, initialsColor, email) {

        let params = {
            key: key,
            imageUrl: imgURL,
            imageInitials: initials,
            text: fullName,
            presence: presence,
            initialsColor: initialsColor,
            email: email,
        }

        return params;
    },


    /**
     * Converts an English language Persona Presence word, such as 'away' or 'online' into 
     * its equivalent Microsoft Fluent presence code. The code for None is the default.
     * @param {string} presence Send in a supported presence status in English, such as 'offline' or 'busy.'.
     * @example 'away'
     * @returns {number} The Microsoft Fluent presence code. The code for None is the default.
     */
    getPersonaPresenceEnumCode: function (presence) {
        let p = presence.trim().toLowerCase();

        switch (p) {
            case "available":                  //Not officially supported
                return this.presenceCodes.online;
            case "away":
                return this.presenceCodes.away;
            case "blocked":
                return this.presenceCodes.blocked;
            case "busy":
                return this.presenceCodes.busy;
            case "dnd":
                return this.presenceCodes.dnd;
            case "none":
                return this.presenceCodes.none;
            case "offline":
                return this.presenceCodes.offline;
            case "online":
                return this.presenceCodes.online;

            default:
                return this.presenceCodes.none;
        }
    },

